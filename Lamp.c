/*
****************************************************************************
       Lamp Control 
****************************************************************************
*/

#include "reg1211.h"
#include "Lamp.h"

sbit Lamp1 = P3^4; // Relay State
sbit Lamp2 = P3^5; // Mode Selection

/*
****************************************************************************
        Lamp On for Test Mode
****************************************************************************
*/
void Lamp_Mode_On(void)  
{
	Lamp1 = 1;
}

/*
****************************************************************************
        Lamp Off for Run Mode
****************************************************************************
*/
void Lamp_Mode_Off(void)
{
	Lamp1 = 0;
}


/*
****************************************************************************
        Lamp On for Relay On State
****************************************************************************
*/
void Lamp_Relay_On(void)  
{
	Lamp2 = 1;
}


/*
***************************************************************************
        Lamp Off for Relay Off State
***************************************************************************
*/
void Lamp_Relay_Off(void)
{
	Lamp2 = 0;
}
